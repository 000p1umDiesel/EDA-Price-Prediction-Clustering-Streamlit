            bed = st.sidebar.number_input('–í–≤–µ–¥–∏—Ç–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å–ø–∞–ª—å–Ω—ã—Ö –∫–æ–º–Ω–∞—Ç', step=1, min_value=1, max_value=100)
