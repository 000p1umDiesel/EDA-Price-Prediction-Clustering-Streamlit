            district = st.sidebar.selectbox('–†–∞–π–æ–Ω', district_options)
            close_to_sights = st.sidebar.selectbox('–ë–ª–∏–∑–æ—Å—Ç—å –∫ –¥–æ—Å—Ç–æ–ø—Ä–∏–º–µ—á–∞—Ç–µ–ª—å–Ω–æ—Å—Ç—è–º', ('–î–∞', '–ù–µ—Ç'))
